def addition(num1, num2):
  return f"Returns the sum of {num1} and {num2}: {num1 + num2}"
def subtraction(num1, num2):
  return f"Returns the difference of {num1} and {num2}: {num1 - num2}"
def multiply(num1, num2):
  return f"Returns the product of {num1} and {num2}: {num1 * num2}"
def modulo(num1, num2):
  return f"Returns the modulo of {num1} and {num2}: {num1 % num2}"
def div(num1, num2):
  return f"Returns the div1, div2 of {num1} and {num2}: {num1 / num2, num1 // num2}"